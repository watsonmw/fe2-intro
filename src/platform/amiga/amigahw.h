#define AMIGA_ADKCON_USE2VN 0x0004
#define AMIGA_ADKCON_USE1VN 0x0002
#define AMIGA_ADKCON_USE0VN 0x0001

#define AMIGA_ADKCONR (AMIGA_CHIP_REG_BASE + 0x9e)

#define AMIGA_AUD0_BASE (AMIGA_CHIP_REG_BASE + 0xa0)
#define AMIGA_AUD0LOC (AMIGA_AUD0_BASE + 0x00)
